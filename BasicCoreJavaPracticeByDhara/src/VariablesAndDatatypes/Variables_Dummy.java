package VariablesAndDatatypes;

public class Variables_Dummy {

	public static void main(String[] args) {
		
	// Integer_Primitive_Datatype
		
		byte b= 2;
		int i = 78;
		short s=10;
		long l=678899;
	
	// Character_Primitive_Datatype
		
		char c='x';
		
	// Decimal_Primitive_Datatype
				
		float f=3.02f;
		double d = 56788.9589;
		
	// Boolean_Primitive_Datatype
		
		boolean b_Val= true;
		
	// Non_Primitive_Datatype
		
		String str= "dhara";
		
		
		System.out.println("Integer_byte="+ b);
		System.out.println("Integer_int="+ i);
		System.out.println("Integer_short="+ s);
		System.out.println(l);
		
		
		System.out.println(c);
		
		System.out.println(f);
		System.out.println(d);
		
		System.out.println(b_Val);
		
		System.out.println(str);
	}

}
